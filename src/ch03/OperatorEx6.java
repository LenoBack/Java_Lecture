package ch03;

/**
 * 대입 연산자
 */
public class OperatorEx6 {
    public static void main(String args[]) {

        int j = 10;
        int k = 20;
        int f = 30;
        int g = 40;
        j += 4; // j = j + 4 (j = 10+4)
        k -= 4; // k = k - 4 (k = 20-4)
        f *= 4; // f = f * 4 (f = 30*4)
        g /= 4; // g = g / 4 (g = 40/4)
        System.out.println(j);
        System.out.println(k);
        System.out.println(f);
        System.out.println(g);

    }
}
